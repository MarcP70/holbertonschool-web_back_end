#!/usr/bin/env python3
"""
Task 2:
Returns the largest integer less than or equal to the input number.
"""
import math


def floor(n: float) -> int:
    """
    Returns the largest integer less than or equal to the input number.

    Args:
        n (float): The input number.

    Returns:
        int: The largest integer less than or equal to the input number.
    """
    return math.floor(n)
