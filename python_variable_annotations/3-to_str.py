#!/usr/bin/env python3
"""
Task 3:
Convert a float number to a string representation.
"""


def to_str(n: float) -> str:
    """
    Convert a float number to a string representation.

    Args:
        n (float): The float number to be converted.

    Returns:
        str: The string representation of the input float number.
    """
    return str(n)
